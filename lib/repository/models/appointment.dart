class Appointment {
  final String fullname;
  final String phone;
  final String address;
  final String date;
  final String time;
  final String quantity;
  final String serviceType;
  final String status;

  Appointment({
    required this.fullname,
    required this.phone,
    required this.address,
    required this.date,
    required this.time,
    required this.quantity,
    required this.serviceType,
    required this.status,
  });
}
